                                  setState(() => _loadingButton = true);
                                  try {
                                    if ((passwordController.text) ==
                                        (loginPageStaffAcountsRecord
                                            .password)) {
                                      await Navigator.pushAndRemoveUntil(
                                        context,
                                        MaterialPageRoute(
                                          builder: (context) =>
                                              HomePageWidget(),
                                        ),
                                        (r) => false,
                                      );
                                    } else {
                                      return;
                                    }
                                  } finally {
                                    setState(() => _loadingButton = false);
                                  }
                                },
                                text: 'Login',
                                options: FFButtonOptions(
                                  width: 130,
                                  height: 50,
                                  color: Color(0xFF4B39EF),
